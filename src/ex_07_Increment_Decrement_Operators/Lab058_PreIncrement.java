package ex_07_Increment_Decrement_Operators;

public class Lab058_PreIncrement {
    public static void main(String[] args) {
        // Pre increment  = increase and then print
        int a = 10;
        int b = ++a;
        System.out.println(a);
        System.out.println(b);

        // ++a ->  a = a+1 , increment by 1

        //  Exp and Result Table
        // Line No | a | Result b
        // line 6 |  10 |  NA
        // line 7  |  11 | 11
        // Line 8   | 11 - print | 11
        // Line 9  | 11 | 11 - pint



    }
}
