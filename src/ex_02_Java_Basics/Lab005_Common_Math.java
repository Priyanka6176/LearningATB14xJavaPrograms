package ex_02_Java_Basics;

public class Lab005_Common_Math {
    public static void main(String[] args) {
        System.out.println(2+2);
        System.out.println(3/2); // Integer division, println will print only integer part and No decimal
        System.out.println(3.0/2); // Decimal division, println will print decimal
        System.out.println(10*2+3-4-5);
        System.out.println(2/2);
        System.out.println(2-2);
        System.out.println("2+2"); // This will print as it because it will treat as a String
        System.out.println(10+3/2-1*5); // Ans is 6>> it will execute as per BODMAS
    }
}
