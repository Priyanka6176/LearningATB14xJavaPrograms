package ex_02_Java_Basics;

public class Lab018_Local_Variables {
    public static void main(String[] args) {
        int a = 10;
        byte b = 10;
        short s = 10;
        long l = 9876543210L;
        float f = 3.14F;
        double d = 67.8987654567;
        boolean b1 = true;
        char c = 'A';
       /* A local variable is declared inside a method, constructor, or block.
          Its lifetime is only within that block/method.
          It must be initialized before use, otherwise will get compiler error.*/
    }
}
